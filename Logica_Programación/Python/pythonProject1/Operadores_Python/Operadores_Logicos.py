# Operadores Logicos Python

a = True
b = False

# Operadores Logico and (Y)
# Regresa verdadero si ambos operandos son verdareos
print(f"{a} and {b} --> {a and b}")

# Operador Logico or (O)
# Regresa verdadero si cualquier operando es verdareo
print(f"{a} or {b} --> {a or b}")

# Operador de Negacion not (NO)
# Invierte el valor original, de True a False, False a True
print(f"not {a} --> {not a}")
