# Variables en Python
# Python es lenguaje dinamico, no nesecitas declarar las variables, el leguaje solo lo intrepreta.

mi_numero = 10
print(mi_numero)
print(type(mi_numero))

mi_numero = 20.5
print(mi_numero)
print(type(mi_numero))

mi_numero = "Hola"
print(mi_numero)
print(type(mi_numero))

mi_numero2 = 30
print(mi_numero2)
print(type(mi_numero2))

mi_numero2 = True
print(mi_numero2)
print(type(mi_numero2))
